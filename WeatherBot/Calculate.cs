        public static string WhichClassOut(int[] conditions)
        {
            int[] conditionClass = new int[4];
            int whichClass = 0, propability = 0;
            Random rnd = new Random();
            int correction = rnd.Next(1, 5);

            foreach (int element in conditions)
            {
                switch (element)
                {
                    case 0:
                        conditionClass[0]++;
                        break;
                    case 1:
                        conditionClass[1]++;
                        break;
                    case 2:
                        conditionClass[2]++;
                        break;
                    case 3:
                        conditionClass[3]++;
                        break;
                }
            }

            for (int i = 0; i < conditionClass.Length; i++)
            {
                if (conditionClass[i] != 0)
                {
                    propability = conditionClass[i];
                    whichClass = i;
                }
            }

            propability = propability * 25 - correction;
            if (whichClass == 1) return "Актированный с 1 по 5 класс с вероятностью " + propability + "%";
            if (whichClass == 2) return "Актированный с 1 по 8 класс с вероятностью " + propability + "%";
            if (whichClass == 3)
                return "Актированный с 1 по 11 класс, а также учащиеся 1 и 2 курсов с вероятностью " +
                       propability + "%";
            return "Актированного не будет с вероятностью " + propability + "%";
        }
